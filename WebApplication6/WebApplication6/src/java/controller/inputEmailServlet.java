    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        new DBContext();
        HttpSession session = request.getSession();
        String forgotPassEmail = request.getParameter("email");;
        Customer cus = CustomerDAO.getCusByEmail(forgotPassEmail);
        try {
            if (cus == null) {
                request.setAttribute("err", "email havent registed");
                request.getRequestDispatcher("inputEmail.jsp").forward(request, response);
            } else {

                String otp = helper.generateRandomString();

                EmailConfig ec = new EmailConfig();
                ec.SendEmail(forgotPassEmail, "Your OTP code " + otp);

                session.setAttribute("otp", otp);
                session.setAttribute("forgotPassEmail", forgotPassEmail);
                session.setAttribute("cus", cus);
                request.getRequestDispatcher("OTPPage.jsp").forward(request, response);

            }
        } catch (MessagingException ex) {
            Logger.getLogger(inputEmailServlet.class.getName()).log(Level.SEVERE, null, ex);
//            request.getRequestDispatcher("ErrorPage.jsp").forward(request, response);
        }

    }

